export function Brzycki_max(weight, reps) {
    return weight * (36 / (37 - reps));
}

export function Brzycki_weight(max, reps) {
    if(reps === 1)
        return max;
    return (max * (37-reps)) / 36;
}

export function Epley_max(weight, reps) {
    return weight * (1 + (0.0333 * reps));
}

export function Epley_weight(max, reps) {
    if(reps === 1)
        return max;
    return max / (1 + 0.0333*reps);
}

export function Lombardi_max(weight, reps) {
    return weight * (reps ** 0.1);
}

export function Lombardi_weight(max, reps) {
    if(reps === 1)
        return max;
    return max / (reps ** 0.1);
}

export function Oconnor_max(weight, reps) {
    return weight * (1 + 0.025 * reps);
}

export function Oconnor_weight(max, reps) {
    if(reps === 1)
        return max;
    return max / (1 + 0.025 * reps);
}

export function GetFormulas(formulaName) {
    switch(formulaName) {
      case 'Brzycki': 
        return {max: Brzycki_max, weight: Brzycki_weight};
      case 'Epley':
        return {max: Epley_max, weight: Epley_weight};
      case 'Lombardi':
        return {max: Lombardi_max, weight: Lombardi_weight};
      case "O'Connor":
        return {max: Oconnor_max, weight: Oconnor_weight};
    }
    return new EvalError(formulaName + ' is not a valid formula');
}

